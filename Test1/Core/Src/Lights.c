void LightsAdjustByIntensity(void){
	uint32_t AD_RES = 0u;
	uint32_t PwmVal = 0u;
	HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		AD_RES = HAL_ADC_GetValue(&hadc1);
		PwmVal = PWM_MAX*AD_RES/(DARKEST_ADC_VAL);
		TIM2->CCR2 = PwmVal;
}
void LightsOff(void){
    TIM2->CCR2 = 0u;
}
